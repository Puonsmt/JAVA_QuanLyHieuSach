    public boolean KiemTraNhapKhachHang(int ts) {
        boolean kiemtra;
        String ID, ten, ngaySinh, gioiTinh, diaChi, email, SDT, ThongBao="";
        ID = "";
        ten = jTF_TenKH.getText();
        diaChi = jTF_DiaChi.getText();
        email = jTF_Email.getText();
        SDT = jTF_SDT.getText();
        
        if(jR_Nam.isSelected()) gioiTinh = jR_Nam.getText();
        else gioiTinh = jR_Nu.getText();
        
        String ngay, thang, nam;
        ngay = jCbb_ngay.getSelectedItem().toString();
        thang = jCbb_thang.getSelectedItem().toString();
        nam = jCbb_nam.getSelectedItem().toString();
        ngaySinh  = nam + "-" + thang + "-" + ngay;
        KhachHang a = new KhachHang(ID, ten, gioiTinh, ngaySinh, diaChi, email, SDT);
        if (a.getTen().equals("")) {
            ThongBao += "Bạn chưa nhập tên Khách hàng!\n";
            lblTenKH.setForeground(Color.red);
        }
        if (a.getDiaChi().equals("")) {
            ThongBao += "Bạn chưa nhập Địa chỉ!\n";
            lblDiaChi.setForeground(Color.red);
        }
        System.out.println(a.getSDT());
        if(!checkSDT(a.getSDT())){
            ThongBao += "Số điện thoại không hợp lệ!\n"; 
            lblSDT.setForeground(Color.red);
        }
        if (a.getEmail().equals("")) {
            ThongBao += "Bạn chưa nhập Email!\n";
            lblEmail.setForeground(Color.red);
        }
        if(contrKH.kiemTraKH(a)){
            ThongBao += "Khách hàng đã được thêm vào trước đó!\n";
        }
        
        if (ThongBao.equals("")) {
            kiemtra = true;
            lblTenKH.setForeground(Color.black);
            lblDiaChi.setForeground(Color.black);
            lblEmail.setForeground(Color.black);
            lblSDT.setForeground(Color.black);
        } else {
            kiemtra = false;
            ThongBao(ThongBao, "Lỗi nhập liệu", 2);
        }
        return kiemtra;
    }
    
