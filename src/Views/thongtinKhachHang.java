    private boolean checkSDT(String s){
        s = s.trim();
        for(int i=0; i<s.length(); i++){
            if(s.charAt(i) < '0' || s.charAt(i) > '9'){             
                System.out.println(s.charAt(i));
                return false;
            }
        }
        return true;
    }
    
