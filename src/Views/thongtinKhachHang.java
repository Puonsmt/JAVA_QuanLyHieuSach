    public void setSelectedCombobox(String cbbselected, JComboBox cbb) {
        int so = Integer.parseInt(cbbselected);
        cbbselected = String.valueOf(so);   
        for (int i = 0; i < cbb.getItemCount(); i++) {
            String obj = String.valueOf(cbb.getItemAt(i));
            if (obj != null) {
                if (cbbselected.trim().equals(obj)) {
                    cbb.setSelectedItem(obj);
                }
            }
        }
    }
    
    public void LayDuLieuKhachHang(ArrayList<KhachHang> listKH) {
        Object[] obj = new Object[]{"STT", "ID KH", "Họ tên", "SDT", "Giới Tính", "Ngày Sinh", "Địa Chỉ", "Email"};
        DefaultTableModel tableModel = new DefaultTableModel(obj, 0);
        jTable1.setModel(tableModel);
        int c = 0;
        for (KhachHang x : listKH){
            Object[] item = new Object[9];
                c++;
                item[0] = c;
                item[1] = x.getID_KH();
                item[2] = x.getTen();
                item[3] = x.getSDT();
                item[4] = x.getGioiTinh();
                item[5] = x.getNgaySinh();
                item[6] = x.getDiaChi();
                item[7] = x.getEmail();
                tableModel.addRow(item);
        }
    }

